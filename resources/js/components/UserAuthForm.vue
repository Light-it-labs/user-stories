<template>
  <div>
    <div class="mt-8 sm:mx-auto sm:w-full sm:max-w-md">
      <div class="bg-white py-8 px-4 shadow sm:rounded-lg sm:px-10">
        <form
          class="space-y-6"
          action=""
          method="post"
          @submit.prevent="checkForm"
        >
          <div v-if="isSignup">
            <label for="name" class="block text-sm font-medium text-gray-700">
              Name
            </label>
            <div class="mt-1">
              <input
                id="name"
                name="name"
                type="name"
                v-model="userInfo.name"
                autocomplete="name"
                
                class="appearance-none block w-full px-3 py-2 border border-gray-300 rounded-md shadow-sm placeholder-gray-400 focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 sm:text-sm"
              />
            </div>
            <div v-if="error.name" class="mt-2">
                <span class="error-text">{{error.name}}</span>
            </div>
          </div>

          <div>
            <label for="email" class="block text-sm font-medium text-gray-700">
              Email address
            </label>
            <div class="mt-1">
              <input
                id="email"
                name="email"
                type="email"
                v-model="userInfo.email"
                autocomplete="email"
                
                class="appearance-none block w-full px-3 py-2 border border-gray-300 rounded-md shadow-sm placeholder-gray-400 focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 sm:text-sm"
              />
            </div>
            <div v-if="error.email" class="mt-2">
                <span class="error-text">{{error.email}}</span>
            </div>
          </div>

          <div v-if="isSignup">
            <label for="image" class="block text-sm font-medium text-gray-700">
              Profile image
            </label>
            <div class="mt-1">
              <input
                id="image"
                name="iamge"
                type="file"
                v-on:change="setFile($event.target.files)"
                
                class="appearance-none block w-full px-3 py-2 border border-gray-300 rounded-md shadow-sm placeholder-gray-400 focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 sm:text-sm"
              />
            </div>
            <div v-if="error.image" class="mt-2">
                <span class="error-text">{{error.image}}</span>
            </div>
          </div>

          <div>
            <label
              for="password"
              class="block text-sm font-medium text-gray-700"
            >
              Password
            </label>
            <div class="mt-1">
              <input
                id="password"
                name="password"
                type="password"
                v-model="userInfo.password"
                autocomplete="current-password"
                
                class="appearance-none block w-full px-3 py-2 border border-gray-300 rounded-md shadow-sm placeholder-gray-400 focus:outline-none focus:ring-indigo-500 focus:border-indigo-500 sm:text-sm"
              />
            </div>
            <div v-if="error.password" class="mt-2">
                <span class="error-text">{{error.password}}</span>
            </div>
          </div>

          <div v-if="!isSignup" class="flex items-center justify-between">
            <div class="flex items-center">
              <input
                id="remember_me"
                name="remember_me"
                type="checkbox"
                v-model="userInfo.remember_me"
                class="h-4 w-4 text-indigo-600 focus:ring-indigo-500 border-gray-300 rounded"
              />
              <label for="remember_me" class="ml-2 block text-sm text-gray-900">
                Remember me
              </label>
            </div>

            <div class="text-sm">
              <a
                href="#"
                class="font-medium text-indigo-600 hover:text-indigo-500"
              >
                Forgot your password?
              </a>
            </div>
          </div>

          <div>
            <button
              type="submit"
              class="w-full flex justify-center py-2 px-4 border border-transparent rounded-md shadow-sm text-sm font-medium text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500"
            >
              {{ buttonText }}
            </button>
          </div>
        </form>
      </div>
    </div>
  </div>
</template>

<script>
import axios from 'axios';
import VueToast from 'vue-toast-notification';

export default {
  data() {
    return {
      userInfo: {
        name: "",
        email: "",
        password: "",
        image: null,
        remember_me: false
      },

      error: {},
    };
  },

  props: ["buttonText", "isSignup"],

  methods: {
    validateEmail: function (email) {
      const re = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
      return re.test(String(email).toLowerCase());
    },

    getBase64(file) {
      return new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.readAsDataURL(file);
        reader.onload = () => resolve(reader.result);
        reader.onerror = error => reject(error);
      });
    },

    setFile([file]){
      this.error.image = "";
      this.getBase64(file).then(data => this.userInfo.image = data);
    },

    async signUp(){
      try{
        const response = await axios.post('/api/auth/signup',this.userInfo);
        Vue.$toast.success(response.data.message);
        window.location.href = '/';

        // const response = await axios.post('/api/auth/signup',formData,{"headers":headers});

      }catch(e){
        Vue.$toast.error(e);
        //Improve and show specific error message. Example: Email already taken.
      }
      
    },

    checkForm: function (e) {
      this.error = {};

      if (this.isSignup && !this.userInfo.name) {
        this.error.name = "The name is required";
      }

      if (!this.validateEmail(this.userInfo.email)) {
        this.error.email = "The email is invalid";
      }

      if(!this.userInfo.image){
        this.error.image = "The image file is required";
      }

      if (!this.userInfo.password) {
        this.error.password = "The password is required";
      }

      if (Object.keys(this.error).length === 0) {
        if(this.isSignup){
          this.signUp();
        }else{
          //call to login function
          console.log("entroooo");
        }
        return true;
      }

      e.preventDefault();
    },
  },

  mounted(){
    Vue.$toast.info("Toasted mounted");
  }
};
</script>

